#!/usr/bin/env node
/**
 * Post-build script: reads Next.js build manifest and injects critical
 * JS/CSS chunk paths into the service worker's STATIC_ASSETS array so
 * they are pre-cached on install (enabling true offline-first PWA).
 *
 * Run after `next build` via: node scripts/inject-precache.js
 */
const fs = require('fs');
const path = require('path');

const BUILD_MANIFEST = path.join(__dirname, '..', '.next', 'build-manifest.json');
const SW_PATH = path.join(__dirname, '..', 'public', 'sw.js');

function run() {
  if (!fs.existsSync(BUILD_MANIFEST)) {
    console.warn('[inject-precache] No .next/build-manifest.json found — skipping.');
    return;
  }

  const manifest = JSON.parse(fs.readFileSync(BUILD_MANIFEST, 'utf8'));

  // Collect critical page chunks: shared entrypoints + the main pages
  const urls = new Set();

  // Shared chunks loaded on every page
  for (const file of manifest.pages['/_app'] || []) {
    urls.add('/_next/' + file);
  }

  // Home page chunks
  for (const file of manifest.pages['/'] || []) {
    urls.add('/_next/' + file);
  }

  // Login page (so offline login page works if cached)
  for (const file of manifest.pages['/login'] || []) {
    urls.add('/_next/' + file);
  }

  if (urls.size === 0) {
    console.warn('[inject-precache] No chunks found in build manifest — skipping.');
    return;
  }

  const urlArray = JSON.stringify([...urls], null, 2);

  let sw = fs.readFileSync(SW_PATH, 'utf8');

  // Inject the precache list as a constant and append it to STATIC_ASSETS
  const injection = `\n// Auto-generated by scripts/inject-precache.js — do not edit manually\nconst BUILD_PRECACHE = ${urlArray};\n`;

  // Insert after the STATIC_ASSETS declaration and modify to include BUILD_PRECACHE
  sw = sw.replace(
    /const STATIC_ASSETS = \[([\s\S]*?)\];/,
    (match, inner) => {
      return match + injection + 'const ALL_PRECACHE = STATIC_ASSETS.concat(BUILD_PRECACHE);';
    }
  );

  // Replace cache.addAll(STATIC_ASSETS) with cache.addAll(ALL_PRECACHE)
  sw = sw.replace('cache.addAll(STATIC_ASSETS)', 'cache.addAll(ALL_PRECACHE)');

  fs.writeFileSync(SW_PATH, sw, 'utf8');
  console.log(`[inject-precache] Injected ${urls.size} chunk URLs into sw.js`);
}

run();
